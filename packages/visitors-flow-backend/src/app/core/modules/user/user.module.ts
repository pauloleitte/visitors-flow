import { BcryptService } from '../../../shared/services/bcrypt.service';

@Module({
  imports: [
    MongooseModule.forFeature([{ name: 'User', schema: UserSchema }]),
  ],
  controllers: [UserController],
  providers: [UserService, BcryptService],
  exports: [UserService]
})
export class UserModule { }
